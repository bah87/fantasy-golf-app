import getTournament from './tournament';
import { getPlayerStats } from './player';

// hardcoded for 2020 US Open
const statsUrl =
  'https://lbdata.pgatour.com/2021/r/026/leaderboard.json?userTrackingId=exp=1600114460~acl=*~hmac=6389c2cc59b9729791d43ffbdae5bb05ada43d16c0a19ac13977d0ea055245ec';

const handleFetchError = (err) => {
  console.log('Fetch error: ', err);
};

const handleFetchTournamentData = (data) => {
  if (!data || !data.leaderboard || !data.leaderboard.players) {
    console.log('No tournament or player data: ', data);
    return;
  }

  return {
    tournament: getTournament({
      ...data.leaderboard,
      lastUpdated: data.last_updated,
    }),
    playerStats: data.leaderboard.players.map(getPlayerStats),
  };
};

export const fetchLeaderboard = () => {
  return fetch(statsUrl).then((data) => data.json().then(handleFetchTournamentData), handleFetchError);
};

